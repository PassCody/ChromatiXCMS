<h2>Was ist ein Cookie?</h2>

<p>
	Ein Cookie ist eine Textdatei mit kleinen Informationsmengen, die auf
	Ihren Computer oder Ihr Mobilgerät beim Besuch einer Website
	heruntergeladen wird. Ein Cookie wird bei jedem neuerlichen Besuch der
	Website oder einer Website, die dieses Cookie erkennt, wieder zurück an
	die ursprüngliche Website gesendet. Cookies sind hilfreich, da Websites
	mithilfe von Cookies das Gerät eines Benutzers erkennen können. Weitere
	Informationen über Cookies finden Sie unter:
	<a href="https://www.allaboutcookies.org" target="_blanck">www.allaboutcookies.org</a>.
</p>

